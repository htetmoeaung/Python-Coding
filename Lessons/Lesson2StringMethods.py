name = 'luffy taro'

print(len(name)) # print the length of the string

print(name.find('a')) # check if the word is in the string

print(name.capitalize()) # capitalize the first word but not capitalize space after

print(name.upper()) # capitalize the whole words

print(name.lower()) # lower the whole words

print(name.isdigit()) # check if the value is digit

print(name.isalpha()) # check if the words is in the alphabet !can not put space

print(name.count('t')) # find the index of the word

print(name.replace('f','g')) # replace the selected word into new word

print(name*5) # print any amount of words with *
